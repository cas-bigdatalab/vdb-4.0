package vdb.webpub.publisher;

import java.util.List;

import cn.csdb.commons.util.ListMap;

public class FileSnapshotList extends ListMap<String, FileSnapshot>
{

	@Override
	public void add(String arg0, FileSnapshot arg1)
	{
		super.add(arg0, arg1);
	}

	@Override
	public void clear()
	{
		super.clear();
	}

	@Override
	public List<FileSnapshot> list()
	{
		return super.list();
	}

	@Override
	public FileSnapshot remove(String arg0)
	{
		return super.remove(arg0);
	}

}
