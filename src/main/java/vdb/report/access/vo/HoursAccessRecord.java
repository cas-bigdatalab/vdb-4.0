package vdb.report.access.vo;

public class HoursAccessRecord
{

	private int counts;

	private String hours;

	public int getCounts()
	{
		return counts;
	}

	public String getHours()
	{
		return hours;
	}

	public void setCounts(int counts)
	{
		this.counts = counts;
	}

	public void setHours(String hours)
	{
		this.hours = hours;
	}

}
